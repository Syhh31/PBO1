package belajar;

/**
 *
 * @author acer
 */
public class Belajar {
    private String name;
    private int age;
    
    public Belajar(String name, int age) {
        this.name = name;
        this.age = age;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
    
    public String getName() {
        return this.name;
    }
    
    public int getAge() {
        return this.age;
    }

}
